import os
import time
import numpy as np
import faiss
from sentence_transformers import SentenceTransformer
from openai import OpenAI
from transformers import AutoTokenizer, AutoModelForCausalLM
import torch

# -------- LOAD DOCUMENTS --------
docs = []
for file in os.listdir("data"):
    with open(f"data/{file}", "r", encoding="utf-8") as f:
        docs.append(f.read())

# -------- CHUNKING --------
def split_chunks(text, size=400, overlap=80):
    chunks = []
    start = 0
    while start < len(text):
        chunks.append(text[start:start+size])
        start += size - overlap
    return chunks

kb = []
for d in docs:
    kb.extend(split_chunks(d))

# -------- EMBEDDINGS --------
embedder = SentenceTransformer("all-MiniLM-L6-v2")
vectors = embedder.encode(kb, normalize_embeddings=True)

index = faiss.IndexFlatIP(vectors.shape[1])
index.add(np.array(vectors))

def retrieve(q, k=3):
    v = embedder.encode([q], normalize_embeddings=True)
    _, ids = index.search(v, k)
    return [kb[i] for i in ids[0]]

# -------- API MODEL --------
client = OpenAI(
    base_url="https://openrouter.ai/api/v1",
    api_key=os.getenv("OPENROUTER_API_KEY")
)

def generate_api(ctx, q):
    context = "\n\n".join(ctx)

    prompt = f"""
You are an expert AI assistant for construction policies.

STRICT RULES:
- Use ONLY the provided context
- Do NOT add outside knowledge
- Answer in short structured bullet points
- If answer missing → say "Information not available in documents"

CONTEXT:
{context}

QUESTION:
{q}

FINAL ANSWER:
"""

    res = client.chat.completions.create(
        model="deepseek/deepseek-chat",
        messages=[{"role":"user","content":prompt}],
        temperature=0.1
    )

    return res.choices[0].message.content

# -------- LOCAL MODEL --------
model_name = "TinyLlama/TinyLlama-1.1B-Chat-v1.0"

tokenizer = AutoTokenizer.from_pretrained(model_name)

local_model = AutoModelForCausalLM.from_pretrained(
    model_name,
    torch_dtype=torch.float32,
    device_map="cpu"
)

def generate_local(ctx, q):
    context = "\n\n".join(ctx)

    prompt = f"""
You are an expert AI assistant for construction policies.

STRICT RULES:
- Use ONLY the provided context
- Do NOT copy text from context
- Summarize in your own words
- Provide short bullet point answers
- If answer missing → say "Information not available in documents"

CONTEXT:
{context}

QUESTION:
{q}

FINAL ANSWER:
"""

    inputs = tokenizer(prompt, return_tensors="pt")

    out = local_model.generate(
        **inputs,
        max_new_tokens=120,
        temperature=0.3,
        do_sample=True,
        repetition_penalty=1.2
    )

    return tokenizer.decode(out[0], skip_special_tokens=True)

# -------- TEST QUESTIONS --------
questions = [
    "How are construction delays managed?",
    "What is escrow payment model?",
    "How does quality assurance work?"
]

for q in questions:

    ctx = retrieve(q)

    print("\n======================")
    print("QUESTION:", q)

    start = time.time()
    api_ans = generate_api(ctx, q)
    print("\nAPI ANSWER:\n", api_ans)
    print("API time:", time.time() - start)

    start = time.time()
    local_ans = generate_local(ctx, q)
    print("\nLOCAL ANSWER:\n", local_ans)
    print("LOCAL time:", time.time() - start)